package com.shree.schools.api

import android.util.Log
import com.shree.schools.data.SchoolData
import okhttp3.HttpUrl
import okhttp3.OkHttpClient
import okhttp3.logging.HttpLoggingInterceptor
import retrofit2.Retrofit
import retrofit2.converter.gson.GsonConverterFactory
import retrofit2.http.GET
import retrofit2.http.Path

interface SchoolApi {

    //https://data.cityofnewyork.us/resource/97mf-9njv.json
    @GET("/resource/{identifier}.json")
    suspend fun getSchools(
        @Path("identifier") identifier: String
    ): List<SchoolData>

    companion object {
        private const val BASE_URL = "https://data.cityofnewyork.us/"

        fun create(): SchoolApi {
            val logger = HttpLoggingInterceptor(HttpLoggingInterceptor.Logger { Log.d("API", it) })
            logger.level = HttpLoggingInterceptor.Level.BASIC

            val client = OkHttpClient.Builder()
                .addInterceptor(logger)
                .build()

            return Retrofit.Builder()
                .baseUrl(HttpUrl.parse(BASE_URL)!!)
                .client(client)
                .addConverterFactory(GsonConverterFactory.create())
                .build()
                .create(SchoolApi::class.java)
        }
    }

}